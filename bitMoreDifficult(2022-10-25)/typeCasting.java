public class typeCasting {
    public static void main(String[] args) {
        double aDoubleVar = 9.6d;

        System.out.println(aDoubleVar);

        System.out.println((int) aDoubleVar); // casting the double into an integer.

        float aFloatVar = (float) aDoubleVar; // casting double var type into a var of type float.

        System.out.println(aFloatVar);
    }
}
